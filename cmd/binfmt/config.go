func allArch() []string {
	out := make([]string, 0, len(configs))
	for name := range configs {
		if _, fullPath, err := getBinaryNames(configs[name]); err == nil {
			if _, err := os.Stat(fullPath); err == nil {
				out = append(out, name)
			}
		} else {
			// Make sure install() will print the error
			out = append(out, name)
		}
	}
	return out
}
